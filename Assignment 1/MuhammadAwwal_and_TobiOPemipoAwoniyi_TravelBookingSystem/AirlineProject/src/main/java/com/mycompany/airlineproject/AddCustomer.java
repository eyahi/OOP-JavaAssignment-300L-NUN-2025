    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
          try {
        // Get the Customer ID to delete
        String Customerid = custID.getText();
        
        if (Customerid.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter a Customer ID to delete");
            return;
        }
        
        // Confirm deletion with user
        int confirm = JOptionPane.showConfirmDialog(null, 
                "Are you sure you want to delete customer with ID: " + Customerid + "?", 
                "Confirm Deletion", 
                JOptionPane.YES_NO_OPTION);
        
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        
        Connection con;
        PreparedStatement pre;
        Class.forName("com.mysql.cj.jdbc.Driver");
        con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlime_project?useSSL=false",
                "root",
                "MamPad200@");
        
        // Prepare delete statement
        pre = con.prepareStatement("DELETE FROM customer WHERE CustomerID = ?");
        pre.setString(1, Customerid);
        
        int rowsAffected = pre.executeUpdate();
        
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Customer deleted successfully");
            // Clear fields after successful deletion
            custID.setText("");
            firstname.setText("");
            lastname.setText("");
            passport.setText("");
            nin.setText("");
            address.setText("");
            contact.setText("");
            male.setSelected(false);
            female.setSelected(false);
            date.setDate(null);
        } else {
            JOptionPane.showMessageDialog(null, "No customer found with ID: " + Customerid);
        }
        
    } catch (ClassNotFoundException ex) {
        Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
    } catch (SQLException ex) {
        Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jButton1ActionPerformed


